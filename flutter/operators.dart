void main() {
  print("hello");

  assert(2 + 3 == 5);
  print("加: 2 + 3 = ${2 + 3}");
  print("减: 5 - 3 = ${5 - 3}");
  print("~/(除后取整) 91 ~/ 8 = ${91 ~/ 8}");
  print("% 取模 91 % 8 = ${91 % 8}");

  // 关系
  print("大于 5 == 2 + 3 = ${5 == 3+2}");
  print("大于 5 > 3 = ${5 > 3}");
  print("小于 5 < 3 = ${5 < 3}");
  print("5 != 3 = ${5 != 3}");
  print("5 <= 3 = ${5 <= 3}");

  // 赋值
  var a = 1;
  var b;
  // b = 5; // 如果存在这一句，下面不会赋值，如果不存在，则会赋值
  b ??= 3;
  print("a = ${a}, b = ${b}");
}
